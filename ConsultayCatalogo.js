function CambiaMunicipio(){
    var Departamento;
    //Se toma el vamor de la "cosa seleccionada"
    Departamento = document.formulario1.Departamento[document.formulario1.Departamento.selectedIndex].value;
    //se chequea si la "cosa" esta definida
    if(Departamento!=0){
        //selecionamos las cosas Correctas
        mis_opts=eval("Municipio_" + Departamento);
        //se calcula el numero de cosas
        num_opts=mis_opts.length;
        //marco el numero de opt en el select
        document.formulario1.Municipio.length = num_opts;
        //para cada opt del array, la pongo en el select
        for(i=0; i<num_opts; i++){
            document.formulario1.Municipio.options[i].value=mis_opts[i];
            document.formulario1.Municipio.options[i].text=mis_opts[i];
        }
        }else{
            //si no habia ninguna opt seleccionada, elimino las cosas del select
            document.formulario1.opt.length = 1;
            //ponemos un guion en la unica opt que he dejado
            document.formulario1.opt.options[0].value="-";
            document.formulario1.opt.options[0].text="-";
        }
        //hacer un reset de las opts
        document.formulario1.Municipio.options[0].selected = true;
        
    }



